# 지도 생성
m = folium.Map(
    location=[37.5665, 126.9780],
    zoom_start=12,
    tiles="OpenStreetMap"
)

# 파란색 마커 추가
for name, info in places.items():

    popup_text = f"""
    <b>{name}</b><br>
    가까운 역: {info['station']}
    """

    folium.Marker(
        location=[info["lat"], info["lon"]],
        popup=popup_text,
        tooltip=name,
        icon=folium.Icon(
            color="blue",
            icon="info-sign"
        )
    ).add_to(m)

# 지도 크기 축소 (60%)
st_folium(m, width=700, height=500)

st.divider()

# 관광지 선택
selected_place = st.selectbox(
    "📌 관광지를 선택하세요",
    list(places.keys())
)

# 선택된 관광지 정보 출력
info = places[selected_place]

st.subheader(f"📍 {selected_place}")
